package com.sekharit.spring.bean;

public class Address {
	private String city;
	private String hno;

	public void setHno(String hno) {
		this.hno = hno;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getCity() {
		return city;
	}

	public String getHno() {
		return hno;
	}
}
