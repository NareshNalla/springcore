package com.sekharit.spring.bean;

public class Parent {
	private String propertyDocs;
	private String bankBalance;
	private String deseases;

	// setters & getters
	public String getPropertyDocs() {
		return propertyDocs;
	}

	public void setPropertyDocs(String propertyDocs) {
		this.propertyDocs = propertyDocs;
	}

	public String getBankBalance() {
		return bankBalance;
	}

	public void setBankBalance(String bankBalance) {
		this.bankBalance = bankBalance;
	}

	public String getDeseases() {
		return deseases;
	}

	public void setDeseases(String deseases) {
		this.deseases = deseases;
	}

}
